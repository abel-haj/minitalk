#include "utils.h"

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	i = 0;
	if (argc > 2)
	{
		j = ft_atoi(argv[2]);
		while (i < j)
		{
			write(1, argv[1], 1);
			i++;
		}
	}
	return (0);
}
